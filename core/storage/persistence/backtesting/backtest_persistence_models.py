from __future__ import annotations

from dataclasses import dataclass
from dataclasses import field
from datetime import date
from datetime import datetime
from decimal import Decimal
from typing import Mapping
from typing import Sequence
from typing import TypeAlias

JsonScalar: TypeAlias = str | int | float | bool | None
JsonValue: TypeAlias = JsonScalar | Mapping[str, "JsonValue"] | Sequence["JsonValue"]
JsonObject: TypeAlias = Mapping[str, JsonValue]
JsonArray: TypeAlias = Sequence[JsonValue]


@dataclass(
    frozen=True,
    slots=True,
)
class BacktestScenarioRecord:
    """
    Typed persistence-boundary record for a curated backtest scenario.
    """

    scenario_id: str
    name: str
    workflow_name: str
    start_date: date
    end_date: date
    symbols: tuple[str, ...]
    benchmark_symbol: str
    initial_cash: Decimal
    provider_profile: str
    initial_positions: JsonArray = ()
    parameters: JsonObject = field(default_factory=dict)
    expected_outcomes: JsonArray = ()
    metadata: JsonObject = field(default_factory=dict)

    def __post_init__(
        self,
    ) -> None:
        _require_non_empty(self.scenario_id, "scenario_id")
        _require_non_empty(self.name, "name")
        _require_non_empty(self.workflow_name, "workflow_name")
        _require_non_empty(self.benchmark_symbol, "benchmark_symbol")
        _require_non_empty(self.provider_profile, "provider_profile")
        if self.start_date > self.end_date:
            raise ValueError("start_date must be on or before end_date.")
        if not self.symbols:
            raise ValueError("symbols cannot be empty.")
        for symbol in self.symbols:
            _require_non_empty(symbol, "symbol")
        if self.initial_cash < Decimal("0"):
            raise ValueError("initial_cash cannot be negative.")


@dataclass(
    frozen=True,
    slots=True,
)
class BacktestRunRecord:
    """
    Typed persistence-boundary record for a completed or failed backtest run.
    """

    backtest_run_id: str
    scenario_id: str
    workflow_name: str
    status: str
    success: bool
    started_at: datetime
    completed_at: datetime
    metrics: JsonObject = field(default_factory=dict)
    metadata: JsonObject = field(default_factory=dict)

    def __post_init__(
        self,
    ) -> None:
        _require_non_empty(self.backtest_run_id, "backtest_run_id")
        _require_non_empty(self.scenario_id, "scenario_id")
        _require_non_empty(self.workflow_name, "workflow_name")
        _require_non_empty(self.status, "status")
        if self.completed_at < self.started_at:
            raise ValueError(
                "completed_at must be greater than or equal to started_at."
            )


