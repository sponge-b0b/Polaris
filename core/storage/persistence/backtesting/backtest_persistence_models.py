    """
    Curated per-step summary linked to an underlying workflow execution id.
    """

    step_id: str
    backtest_run_id: str
    step_index: int
    timestamp: datetime
    workflow_run_id: str
    success: bool
    node_output_keys: tuple[str, ...] = ()
    summary: JsonObject = field(default_factory=dict)

    def __post_init__(
        self,
    ) -> None:
        _require_non_empty(self.step_id, "step_id")
        _require_non_empty(self.backtest_run_id, "backtest_run_id")
        _require_non_negative(self.step_index, "step_index")
        _require_non_empty(self.workflow_run_id, "workflow_run_id")
        for node_key in self.node_output_keys:
            _require_non_empty(node_key, "node_output_key")


