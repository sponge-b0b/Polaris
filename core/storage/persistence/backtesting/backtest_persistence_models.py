@dataclass(
    frozen=True,
    slots=True,
)
class BacktestPersistenceResult:
    """
    Typed result returned by backtest persistence adapters.
    """

    success: bool
    records_persisted: int = 0
    backtest_run_id: str | None = None
    error: str | None = None

    def __post_init__(
        self,
    ) -> None:
        _require_non_negative(self.records_persisted, "records_persisted")
        if self.success and self.error is not None:
            raise ValueError("successful persistence results cannot include an error.")
        if self.success:
            _require_non_empty(self.backtest_run_id, "backtest_run_id")
        if not self.success:
            _require_non_empty(self.error, "error")

    @classmethod
    def succeeded(
        cls,
        *,
        backtest_run_id: str,
        records_persisted: int = 1,
    ) -> BacktestPersistenceResult:
        return cls(
            success=True,
            records_persisted=records_persisted,
            backtest_run_id=backtest_run_id,
        )

    @classmethod
    def failed(
        cls,
        error: str,
    ) -> BacktestPersistenceResult:
        return cls(
            success=False,
            error=error,
        )


def _require_non_empty(
    value: str | None,
    field_name: str,
) -> None:
    if value is None or not value.strip():
        raise ValueError(f"{field_name} cannot be empty.")


def _require_non_negative(
    value: int,
    field_name: str,
) -> None:
    if value < 0:
        raise ValueError(f"{field_name} cannot be negative.")


def _require_timezone_aware(
    value: datetime,
    field_name: str,
) -> None:
    if value.tzinfo is None or value.utcoffset() is None:
        raise ValueError(f"{field_name} must be timezone-aware.")
